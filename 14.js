//For Loop

/*statement index pertama dieksekusi sebelum proses (berisi nilai awal),
statement kedua akan membandingkan index dengan bilangan numerik lain
bertujuan mengukur banyaknya perulangan yang akan dieksekusi, 
statement ketiga bertujuan untuk eksekusi setiap kali 
statement kedua bernilai true serta terjadinya perubahan nilai index
hingga statement berni=lai false*/
for(let index = 1; index <= 10; index++) {
    console.log("Perulangan ke-" + index);
}
//terjadi 10 looping karena kondisi akan bernilai false saat index > 10